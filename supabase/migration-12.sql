-- migration-12 — 새 계정을 기본 '공개 계정'으로
--
-- 실행: Supabase Dashboard → SQL Editor → New query → 붙여넣고 Run
--
-- 배경:
--   handle_new_user 트리거는 is_public을 넘기지 않으므로 컬럼 기본값이 그대로 적용된다.
--   지금까지 기본값이 false라, 가입한 사람은 설정 탭을 직접 찾아 들어가 켜야 했다.
--   지인 베타에서 이 단계를 넘는 사람이 거의 없을 것으로 보고 기본값을 뒤집는다.
--
-- 공개 계정이 되면 달라지는 것:
--   · 프로필과 지난 허브 사진을 친구가 아닌 사람도 볼 수 있다
--   · 지난 글이 '전체' 탭에 남는다 (비공개면 오늘 글만 노출)
--   설정 → 앱 → '공개 계정' 토글로 언제든 되돌릴 수 있고, 끄면 지난 글은 즉시 숨겨진다.

-- ---------------------------------------------------------------
-- 1. 앞으로 가입하는 계정의 기본값
-- ---------------------------------------------------------------
alter table public.profiles
  alter column is_public set default true;

-- ---------------------------------------------------------------
-- 2. 이미 만들어진 계정도 공개로 전환
--    지인 베타 시작 전이라 실계정은 운영자 본인뿐이다.
--    ⚠️ 이미 사용자가 스스로 비공개를 선택한 뒤라면 이 문장은 빼고 실행할 것 —
--       본인이 내린 결정을 서버가 되돌리는 셈이 된다.
-- ---------------------------------------------------------------
update public.profiles
  set is_public = true
  where is_public = false;
