-- 주간 주제 승인 — 매주 한 번, 다음 7일의 주제를 검토·수정한 뒤 실행
-- 실행: Supabase Dashboard → SQL Editor → New query → 붙여넣고 Run
--
-- 사용법:
--   1. 아래 날짜를 다음 주 날짜로 바꾼다 (허브 날짜는 UTC 기준 = 한국시간 오전 9시에 전환)
--   2. 주제 문구를 원하는 대로 수정한다 (이게 곧 '승인')
--   3. Run — 이미 있는 날짜는 새 주제로 덮어씀
--
-- 주제가 등록되지 않은 날은 앱에 "오늘의 주제를 준비하고 있어요"가 표시되고
-- 사진 게시가 잠기므로, 매주 잊지 말고 실행할 것.
--
-- 아이디어 풀 (backend.js TOPIC_POOL): 오늘 내가 지나친 작은 장면 / 오늘 가장 오래 머문 자리 /
-- 지금 창밖의 빛 / 오늘 마신 것 / 걷다가 멈춘 순간 / 오늘의 하늘 한 조각 / 손에 들고 있던 것 /
-- 오늘 나를 웃게 한 장면 / 집으로 가는 길 / 오늘의 저녁 풍경 / 책상 위 한 뼘 /
-- 기다리는 동안 본 것 / 오늘 처음 본 것 / 발밑의 풍경 / 지금 이 순간의 소음

insert into public.hubs (hub_date, topic) values
  ('2026-07-10', '오늘 나를 미소짓게 한'),
  ('2026-07-11', '오늘 하늘과 구름'),
  ('2026-07-12', '걷다가 멈춘 순간'),
  ('2026-07-13', '오늘 마신 것'),
  ('2026-07-14', '오늘의 작업'),
  ('2026-07-15', '집으로 가는 길'),
  ('2026-07-16', '오늘 처음 본 것')
on conflict (hub_date) do update set topic = excluded.topic;
